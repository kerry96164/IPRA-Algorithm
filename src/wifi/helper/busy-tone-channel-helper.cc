Ptr<BusytoneChannel>
BusytoneChannelHelper::Create (void) const
{
  Ptr<BusytoneChannel> channel = CreateObject<BusytoneChannel> ();
  Ptr<PropagationLossModel> prev = 0;
  for (std::vector<ObjectFactory>::const_iterator i = m_propagationLoss.begin (); i != m_propagationLoss.end (); ++i)
    {
      Ptr<PropagationLossModel> cur = (*i).Create<PropagationLossModel> ();
      if (prev != 0)
        {
          prev->SetNext (cur);
        }
      if (m_propagationLoss.begin () == i)
        {
          channel->SetPropagationLossModel (cur);
        }
      prev = cur;
    }
  /*Ptr<PropagationDelayModel> delay = m_propagationDelay.Create<PropagationDelayModel> ();///check
  channel->SetPropagationDelayModel (delay);////check*/
  return channel;
}

